window.onload = function () {
    var WRAPPER = document.querySelector('#wrapper');
    var CAMERA, SCENE, RENDERER;
    var CONTROLS;
    var OBJECTS = [];
    var TARGETS = { table: [], sphere: [], helix: [], circle: [], grid: [] };
    var ROTATE_SPEED = 0.015;

    init();
    animate();

    function init () {
        CAMERA = new THREE.PerspectiveCamera(40, WRAPPER.clientWidth / WRAPPER.clientHeight, -10000, 10000);
        CAMERA.position.set(0, 0, 3000);

        SCENE = new THREE.Scene();

        // objects
        TABLE.forEach((item, i) => {

            var el = el2Obj(item);

            var obj = new THREE.CSS3DObject(el);
            obj.position.x = Math.random() * 4000 - 2000;
            obj.position.y = Math.random() * 4000 - 2000;
            obj.position.z = Math.random() * 4000 - 2000;

            SCENE.add(obj);

            OBJECTS.push(obj);
        });

        // tables
        OBJECTS.forEach((_, i) => {
            let ob = new THREE.Object3D();
            ob.position.x = ~~(i / 8) * 150 + 100;
            ob.position.y = -(~~(i % 8) * 180) + 990;

            TARGETS.table.push(ob);
        });

        // sphere
        var vector = new THREE.Vector3();
        for (let i = 0, l = OBJECTS.length; i < l; i++) {
            var phi = Math.acos(-1 + (2 * i) / l);
            var theta = Math.sqrt(l * Math.PI) * phi;

            var obj = new THREE.Object3D();
            obj.position.setFromSphericalCoords(1000, phi, theta);

            vector.copy(obj.position).multiplyScalar(2);

            obj.lookAt(vector);

            TARGETS.sphere.push(obj);
        }

        // helix
        for (let i = 0, l = OBJECTS.length; i < l; i++) {
            let theta = i * 0.175 + Math.PI;
            let y = -(i * 8) + 450;
            let obj = new THREE.Object3D();
            obj.position.setFromCylindricalCoords(900, theta, y);
            vector.x = obj.position.x * 2;
            vector.y = obj.position.y;
            vector.z = obj.position.z * 2;

            obj.lookAt(vector);

            TARGETS.helix.push(obj);
        }
        const R = OBJECTS.length * 100 / Math.PI;
        const RA = 1.08;
        // circle
        OBJECTS.forEach((item, i) => {
            let theta = i / OBJECTS.length * 2 * Math.PI;
            let y = 0;
            let obj = new THREE.Object3D();
            obj.position.setFromCylindricalCoords(R, theta, y);

            vector.x = obj.position.x * 2;
            vector.y = obj.position.y * 2;
            vector.z = obj.position.z * 2;

            obj.lookAt(vector);

            TARGETS.circle.push(obj);
        });

        // grid
        OBJECTS.forEach((_, i) => {
            let obj = new THREE.Object3D();
            obj.position.x = ((i % 5) * 400) - 800;
            obj.position.y = (-(Math.floor(i / 5) % 5) * 400) + 800;
            obj.position.z = (Math.floor(i / 11)) * 1000 - 2000;

            TARGETS.grid.push(obj);
        });

        RENDERER = new THREE.CSS3DRenderer();
        RENDERER.setSize(WRAPPER.clientWidth, WRAPPER.clientHeight);

        WRAPPER.appendChild(RENDERER.domElement);

        CONTROLS = new THREE.TrackballControls(CAMERA, RENDERER.domElement);

        CONTROLS.rotateSpeed = 0.5;
        CONTROLS.minDistance = 500;
        CONTROLS.maxDistance = 10000;

        CONTROLS.addEventListener('change', render);

        // init state
        // CAMERA.position.set(0, 0, R + 280);
        // transform(TARGETS.circle, 2000);
        transform(TARGETS.sphere, 2000);

        var button = document.querySelector('#rotate');
        button.addEventListener('click', function (event) {
            console.log('aouet');
            ROTATE_SPEED = Math.floor(0.1);

        });
        var button = document.querySelector('#rotateu');
        button.addEventListener('click', function (event) {
            const timer = setInterval(_ => {
                if (ROTATE_SPEED >= 0.115) {
                    ROTATE_SPEED = 0.115;
                    clearInterval(timer);
                }

                ROTATE_SPEED += 0.0025;
            }, 16);
        });
        var button = document.querySelector('#rotated');
        button.addEventListener('click', function (event) {
            const timer = setInterval(_ => {
                if (ROTATE_SPEED <= 0) {
                    clearInterval(timer);
                }
                ROTATE_SPEED = ((ROTATE_SPEED * 1e5 - 0.0026e5) / 1e5);
            }, 16);
        });
        var button = document.getElementById('table');
        button.addEventListener('click', function (event) {
            transform(TARGETS.table, 2000);
        }, false);
        var button = document.getElementById('sphere');
        button.addEventListener('click', function (event) {
            transform(TARGETS.sphere, 2000);
        }, false);
        var button = document.getElementById('helix');
        button.addEventListener('click', function (event) {
            transform(TARGETS.helix, 2000);
        }, false);

        var button = document.getElementById('look');
        button.addEventListener('click', function (event) {
            const rd = ~~(Math.random() * OBJECTS.length);
            const position = TARGETS.circle[rd].position;
            CAMERA.position.x = position.x * RA;
            CAMERA.position.y = position.y;
            CAMERA.position.z = position.z * RA;
            console.log(position);
        }, false);
        var button = document.getElementById('circle');
        button.addEventListener('click', function (event) {
            transform(TARGETS.circle, 2000);
        }, false);
        var button = document.getElementById('grid');
        button.addEventListener('click', function (event) {
            transform(TARGETS.grid, 2000);
        }, false);
        var button = document.querySelector('#draw');
        button.addEventListener('click', function (event) {
            let timer;
            if (timer) {
                return;
            }
            timer = setInterval(_ => {
                if (ROTATE_SPEED >= 0.115) {
                    ROTATE_SPEED = 0.115;
                    clearInterval(timer);
                    timer = setTimeout(_ => {
                        transform(TARGETS.helix, 2000);
                        clearTimeout(timer);
                        timer = setTimeout(_ => {
                            transform(TARGETS.circle, 3000);
                            let ct = setTimeout(_ => {
                                clearTimeout(ct);
                                ct = null;
                                CAMERA.position.set(0, 0, R * RA);
                            }, 1500);
                            let dt = setTimeout(_ => {
                                clearTimeout(dt);
                                timer = setInterval(_ => {
                                    if (ROTATE_SPEED <= 0.015) {
                                        ROTATE_SPEED = 0.015;
                                        clearInterval(timer);
                                    }
                                    ROTATE_SPEED = ((ROTATE_SPEED * 1e5 - 0.0052e5) / 1e5);
                                }, 16);
                            }, 4000);
                        }, 4000);
                    }, 2000);
                }
                ROTATE_SPEED += 0.00125;
            }, 32);
        });

        var button = document.querySelector('#stop');
        button.addEventListener('click', function () {

            const rd = ~~(Math.random() * OBJECTS.length);
            console.log(rd);
            const position = TARGETS.circle[rd].position;
            CAMERA.position.x = position.x * RA;
            CAMERA.position.y = position.y;
            CAMERA.position.z = position.z * RA;
            ROTATE_SPEED = 0;
        });

    }

    function transform (targets, duration) {
        TWEEN.removeAll();
        targets.forEach((target, i) => {
            let obj = OBJECTS[i];
            new TWEEN.Tween(obj.position)
                .to(
                    {
                        x: target.position.x,
                        y: target.position.y,
                        z: target.position.z
                    }, Math.random() * duration + duration
                )
                .easing(TWEEN.Easing.Exponential.InOut)
                .start();
            new TWEEN.Tween(obj.rotation)
                .to(
                    {
                        x: target.rotation.x,
                        y: target.rotation.y,
                        z: target.rotation.z
                    }, Math.random() * duration + duration
                )
                .easing(TWEEN.Easing.Exponential.InOut)
                .start();
        });

        new TWEEN.Tween(this)
            .to({}, duration * 2)
            .onUpdate(render)
            .start();

    }

    function animate () {
        requestAnimationFrame(animate);

        var vector = CONTROLS.target.clone();
        var up = CAMERA.up.clone();
        var quaternion = new THREE.Quaternion();
        if (ROTATE_SPEED <= 0) {
            ROTATE_SPEED = 0;
        }
        quaternion.setFromAxisAngle(up, ROTATE_SPEED);
        CAMERA.position.applyQuaternion(quaternion);
        CAMERA.lookAt(vector);

        TWEEN.update();

        CONTROLS.update();
    }

    function render () {
        RENDERER.render(SCENE, CAMERA);
    }

    function el2Obj (item, color) {
        var el = document.createElement('div');
        el.className = 'element';
        el.style.backgroundColor = color ? ('rgba(0, 127,127, ' + (Math.random() * 0.5 + 0.25) + ')') : ('rgba(127, 0,127, ' + (Math.random() * 0.5 + 0.25) + ')');

        var sy = document.createElement('div');
        sy.className = 'symbol';
        sy.textContent = item.nick[0];
        el.appendChild(sy);

        var de = document.createElement('div');
        de.className = 'details';
        de.innerHTML = item.tel;
        el.appendChild(de);

        return el;
    }
};
